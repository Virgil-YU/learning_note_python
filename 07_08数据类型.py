"""

数值形： int（整形）；float（浮点型(小数)）
布尔型： True（真）；False（假）
字符串： str
列表： list
元组： tuple
集合： set
字典： dict

"""

# 验证数据类型 type

num1 = 1  # int
num2 = 1.1  # float
a = "hello world"
b = True
c = [10, 20, 30]  # list----列表
d = (10, 20, 30)  # tuple-----元组
e = {10, 20, 30}  # set----集合
f = {"name": "tom", "age": 18}  # dict----字典----键值对

print(type(num1))
print(type(num2))
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
